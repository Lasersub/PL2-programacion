    private void jButtonMisEventosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMisEventosActionPerformed
        // TODO add your handling code here:
        // Crear el popup menu
        JPopupMenu popupMenu = new JPopupMenu();
        
        // Crear las opciones
        JMenuItem itemReservados = new JMenuItem("EVENTOS RESERVADOS");
        JMenuItem itemAtendidos = new JMenuItem("EVENTOS ATENDIDOS");
        
        // Añadir acciones a las opciones
        itemReservados.addActionListener(e -> {
            Navegacion.cambiarVentana(this, new ConsultarReserva(cliente));
        });

        itemAtendidos.addActionListener(e -> {
            JOptionPane.showMessageDialog(this, "Mostrando eventos atendidos...");
        });
        
        // Agregar ítems al menú
        popupMenu.add(itemReservados);
        popupMenu.add(itemAtendidos);
        
        // Mostrar el popup en la posición del botón
        popupMenu.show(jButtonMisEventos, 3, jButtonMisEventos.getHeight());
    }//GEN-LAST:event_jButtonMisEventosActionPerformed

